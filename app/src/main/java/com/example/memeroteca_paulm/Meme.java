package com.example.memeroteca_paulm;

public class Meme {

    private int id;
    private String name;
    private String url;
    private int width;
    private int height;
    private int box_count;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getBox_count() {
        return box_count;
    }
}
